# tuple function

tuple1 = tuple()
print("empty tuple:", tuple1)

list1= [ 1, 2, 3, 4 ]
tuple2 = tuple(list1)
print("list to tuple:", tuple2)


dict = { 1 : 'one', 2 : 'two' }
tuple3 = tuple(dict)
print("dict to tuple:", tuple3)

string = "Hello Python"
tuple4 = tuple(string)
print("str to tuple:", tuple4)
